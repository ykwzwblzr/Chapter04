#include <iostream>
using namespace std;
int main()
{  double r=0,d=0,c=0,s=0;
   cin>>r;
   d=2*r;
   c=2*r*3.14159;
   s=3.14159*r*r;
   cout<<"直径是："<<d<<"\n"<<"周长是："<<c<<"\n"<<"面积是："<<s<<endl;

}
